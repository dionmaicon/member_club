    public void organizaTabela() {
        
        DefaultTableModel model = (DefaultTableModel) TabelaReservas.getModel();
        model.getDataVector().clear();
        RNReserva rn = new RNReserva();
        
        list = rn.getTodasReservasMes(Integer.parseInt(tfMes.getText()));
  
        if(list.isEmpty()){
            //model.addColumn(new Object[]{""+ 0,"Joao da Silva" , "000.000.000-00" });
        }else{
            for (Reserva reserva : list) {
                
                model.addRow(new Object[] {""+ reserva.getData_reserva() , reserva.getHora_reserva()+":00 ", reserva.getNome_dos_usuarios()});
            }
            list.clear();
        }
        TabelaReservas.setModel(model);
       
    }
   

